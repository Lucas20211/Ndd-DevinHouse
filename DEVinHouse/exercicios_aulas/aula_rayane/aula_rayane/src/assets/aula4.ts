

export class Calculadora {

    soma(num1:number, num2:number):number {
        return num1 + num2;
    }
    subtracao(num1:number, num2:number):number {
        return num1 - num2;
    }
    divisao(num1:number, num2:number):number {
        return num1 / num2;
    }
    multiplicacao(num1:number, num2:number):number {
            return num1 * num2;
    }
}